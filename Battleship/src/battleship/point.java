package battleship;

/**
 *
 * @author peyman
 */
public class point { 
   private int x;
   private int y;
   point(int x, int y){
       this.x= x;
       this.y= y;
   }

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }

    
}
